#include <stdio.h>

int	ft_atoi(char *str)
{
	int	i;
	int	j;
	int	s;

	i = 0;
	j = 0;
	s = 1;
	while (str[i] <= 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			s *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		j = j * 10 + str[i] - '0';
		i++;
	}
	return (s * j);
}
